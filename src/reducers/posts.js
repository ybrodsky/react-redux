import {
	REQUEST_POSTS,
	RECEIVE_POSTS,
	SELECT_SUBREDDIT,
	INVALIDATE_SUBREDDIT,
} from './../constants/types';

const defaultState = {
  isFetching: false,
  didInvalidate: false,
  items: []
};

const posts = (state = defaultState, action) => {
  switch (action.type) {
    case INVALIDATE_SUBREDDIT:
      return Object.assign({}, state, {
        didInvalidate: true
      })
    case REQUEST_POSTS:
      return Object.assign({}, state, {
        isFetching: true,
        didInvalidate: false
      })
    case RECEIVE_POSTS:
      return Object.assign({}, state, {
        isFetching: false,
        didInvalidate: false,
        items: action.posts,
        lastUpdated: action.receivedAt
      })
    default:
      return state
  }
};
